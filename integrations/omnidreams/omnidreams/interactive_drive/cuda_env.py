from __future__ import annotations

import os

DISABLE_CUDA_INTEROP_ENV = "INTERACTIVE_DRIVE_DISABLE_CUDA_INTEROP"


def env_truthy(name: str) -> bool:
    value = os.environ.get(name)
    if value is None:
        return False
    return value.strip().lower() in {"1", "true", "yes", "on"}
